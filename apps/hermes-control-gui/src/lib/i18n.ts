type TranslationKey = keyof (typeof translations)[typeof DEFAULT_LANGUAGE];

export function normalizeLanguage(value: string | null | undefined): LanguageId {
  return value === 'en-US' ? 'en-US' : DEFAULT_LANGUAGE;
}

export function createTranslator(language: LanguageId) {
  return (key: TranslationKey): string => translations[language][key] ?? translations[DEFAULT_LANGUAGE][key];
}
